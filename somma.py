"scrivi funzioni per il calcolo elementare di due numeri"

def sum(a,b):
    return a+b

def dif(a,b):
    return a-b

def prod(a,b):
    return a*b

def div(a,b):
    return a//b


a=5
b=7

print("la somma è: ", sum(a,b))
print("la differenza è: ", dif(a,b))
print("il prodotto è:", prod(a,b))
print("la divisione è:", div(a,b))

